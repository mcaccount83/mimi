# New Chapter Application

Your Application has been Successfully Submitted!

Here are a few things to keep in mind as you start your MOMS Club journey.

- All chapters are in PENDING status until reviewed by our Coordinator Team.
- After review, you will receive an email from your Coordinator to establish initial communication as well
as verify/set your official chapter name and boundaries.
- After communication has been established, your credit card will be charged, your chapter will move to
ACTIVE status and your official MOMS Club chapter manual will be shipped.
- There are no refunds after the payment has been processed.
- Check your application status any time by logging into MIMI with the credentials you set up during the
application process.
- In MIMI you will also see your Coordinator's contact information. If you do not hear from them within a
week of submitting your application, please reach out to them directly as sometimes messages do end up in spam.
- After your chapter has moved to ACTIVE status you'll see your MIMI options change to allow more access and
information, but your login credentials will remain the same.

Log into MIMI: [http://momsclub.org/mimi](http://momsclub.org/mimi)
Return to the Main Website: [http://momsclub.org](http://momsclub.org)

