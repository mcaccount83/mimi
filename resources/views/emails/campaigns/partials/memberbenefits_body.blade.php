<h1><center>Nonprofit Status & Member Benefits</center></h1>
<br>
<p><b><u>Your Nonprofit Status</u></b><br>
    The International MOMS Club is a 501(C)(3) public charity, registered with the IRS under the educational and charitable categories. The 501(C)(3)
    classification is the highest classification of nonprofit organization recognized by the IRS. There are sereral things about being a nonprofit that
    you should be aware of.<br>
<ul>
    <li>All MOMS Club chapters that are properly registered with the International MOMS Club are covered under the International MOMS Club's nonprofit group
        exemption. Our group exemption number is: 3706. That number confirms that all of our chapters are also recognized by the IRS as 501(C)(3) public charities.</li>
    <li>If you need a copy of our group exemption letter, you may download it from the Resources section in MIMI.</li>
    <li>The IRS requires that nonprofit organizations like your chapter file an online 990N e-Postcard each year. Filing is easy and is the responsibility of the
        local chapter. Any chapter that fails to submit the postcard may lose their nonprofit status by the IRS! You will need to fill out the form online, and instructions
        will be posted on our website July 1st, the start of the new MOMS Club fiscal year.</li>
    <li>Pay attention to any emails or letters you may receive from the IRS or your state. If you have any questions about anything from the IRS or your state, contact
        your Coordinator.</li>
</ul>
</p>
<p><b><u>Party Expenses/Member Benefits</u></b><br>
    To retain our nonprofit status with the IRS, all chapters must spend no more of their treasury than 15% of their DUES income for parties, outings or any other
    activities or gifts that directly benefit only their members. Chapters may spend less than 15%, but under no circumstances may they spend more than 15% of
    their dues income on parties or things that directly benefit only their members or members’ families.<br>
<ul>
    <li>This does include any parties, crafts for children at parties, outings with children, or member-only fun activities.</li>
    <li>This would not include a modest expense for activities at regular meetings where new mothers also attend without being members yet.</li>
    <li>Costs of attending training sessions or educational activities for the mothers (president's meetings, MOMS Club State/Regional International
        Luncheons, parenting or CPR classes, etc.) are not included in the 15%.</li>
    <li>And, any parties, activities or outings where the whole cost is paid directly by the members attending, and no money is being taken from the
        treasury do not count in the 15%.</li>
    <li>Modest appreciation gifts to members for their service to the club, such as outgoing board member recognitions, may be purchased by the chapter, with a
        vote of the membership. If those thank-you recognitions are modest in cost, they should not be counted by the IRS as member-benefit expenses. However, if
        the recognitions are not modest thank-you’s for real volunteering to the chapter, the IRS could decide that those were gifts that benefited the members,
        and therefore would be included in the “party expense” 15%.</li>
</ul>
</p>
