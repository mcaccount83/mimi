<p><b>What is a Big Sister?</b><br>
    A Big Sister is a new Primary Coordinator and is the first person that a chapter will reach out to
    when they want to speak with International. A Big Sister is a vital connection between the Chapter
    and International. We LOVE for chapters to think of their Big Sister/Primary Coordinator as an
    extension of their chapter, to include them in their newsletter distribution, to reach out to them
    when they have wonderful news to share, to ask them when they have questions that they are unsure of,
    or find themselves in situations where they need a bit of extra guidance. A Big Sister should strive
    to be a positive beacon for their chapters and someone the chapter feels comfortable coming to for
    anything that they may need -- big or small!</p>
<br>
<p><b>Who can be a Big Sister?</b><br>
    Any current or past board member can be a Big Sister! There have also been instances where a really
    great Activity Group Coordinator, who was never on the board, has been recommended by a chapter
    President to become a Big Sister as well. So, while being on the board isn't a 100% requirement,
    it is the typical route.</p>
<br>
<p><b>What do I need to know to be a Big Sister?</b><br>
    Nothing more than you do right now. Even if you haven't experienced a crazy amount of issues or
    situations in your own chapter, as a board member you have been exposed to the basic operations and
    expectations for chapters. We'll teach you the rest! And, since so much of what we see is specific
    to individual situations, there is a lot of "on the job training". But you are NEVER alone. You will
    always have a Mentoring Coordinator who is there to help guide you. No matter how long you have been
    a volunteer, you'll always have someone you can go to with questions or for advice!</p>
<br>
<p><b>Why would you want ME to be a Big Sister?</b><br>
    Everyone has something to offer - and that includes YOU! We all bring unique perspectives, gifts and
    talents to the MOMS Club. This is true in our own chapters as well as with International. Don't
    think that you don't have something to offer at the International level -- YOU DO -- and we'd love
    to have you join our team!</p>
<br>
<p><b>When can I apply to be a Big Sister?</b><br>
    NOW! You can apply to volunteer with International at any time during the year. Unlike a board
    position, you can start a Big Sister role at any time and are not restricted to a 12
    month July - June window.</p>
<br>
